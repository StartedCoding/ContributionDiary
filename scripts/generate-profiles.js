import fs from "fs";
import path from "path";

const ROOT = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..");
const PARSED = path.join(ROOT, "scripts", "_parsed.json");

function computeStreak(entries) {
  if (!entries.length) return { current: 0, longest: 0 };

  const dates = [...new Set(entries.map((e) => e.date))].sort();
  let current = 1;
  let longest = 1;
  let streak = 1;

  const today = new Date().toISOString().slice(0, 10);
  const yesterday = new Date(Date.now() - 86400000).toISOString().slice(0, 10);
  const lastDate = dates[dates.length - 1];

  // Streak only counts if logged today or yesterday
  const isActive = lastDate === today || lastDate === yesterday;

  for (let i = 1; i < dates.length; i++) {
    const diff = daysBetween(dates[i - 1], dates[i]);
    if (diff === 1) {
      streak++;
      longest = Math.max(longest, streak);
    } else {
      streak = 1;
    }
  }

  return {
    current: isActive ? streak : 0,
    longest,
  };
}

function daysBetween(a, b) {
  return Math.round((new Date(b) - new Date(a)) / 86400000);
}

function topTags(entries, n = 5) {
  const count = {};
  for (const e of entries) {
    for (const tag of e.tags) count[tag] = (count[tag] || 0) + 1;
  }
  return Object.entries(count)
    .sort((a, b) => b[1] - a[1])
    .slice(0, n)
    .map(([tag, c]) => `\`${tag}\` ×${c}`)
    .join(" · ");
}

function recentEntries(entries, n = 5) {
  return [...entries]
    .reverse()
    .slice(0, n)
    .map((e) => `| ${e.date} | ${e.project} | ${e.type} | ${e.time_spent}h | ${e.tags.map((t) => `\`${t}\``).join(" ")} |`)
    .join("\n");
}

function generateProfile(user) {
  const { username, entries } = user;
  const streak = computeStreak(entries);
  const totalHours = Math.round(entries.reduce((s, e) => s + e.time_spent, 0));
  const totalEntries = entries.length;
  const activeDays = new Set(entries.map((e) => e.date)).size;
  const projects = [...new Set(entries.map((e) => e.project))].filter((p) => p !== "unknown");
  const lastActive = entries.length ? entries[entries.length - 1].date : "—";
  const tags = topTags(entries);
  const recent = recentEntries(entries);
  const updated = new Date().toISOString().slice(0, 10);

  return `# ${username} — ContributionDiary

> Auto-generated profile · Last updated ${updated}

## Stats

| | |
|---|---|
| 🔥 Current Streak | ${streak.current} day${streak.current !== 1 ? "s" : ""} |
| 🏆 Longest Streak | ${streak.longest} day${streak.longest !== 1 ? "s" : ""} |
| 📝 Total Entries | ${totalEntries} |
| ⏱️ Hours Tracked | ${totalHours}h |
| 📅 Active Days | ${activeDays} |
| 🕐 Last Active | ${lastActive} |

## Top Tags

${tags || "No tags yet."}

## Projects

${projects.length ? projects.map((p) => `- \`${p}\``).join("\n") : "No projects logged yet."}

## Recent Entries

| Date | Project | Type | Time | Tags |
|------|---------|------|------|------|
${recent || "| — | — | — | — | — |"}

---

*This file is auto-generated by [ContributionDiary](../../README.md). Do not edit manually.*
`;
}

function generateProfiles() {
  const users = JSON.parse(fs.readFileSync(PARSED, "utf-8"));

  for (const user of Object.values(users)) {
    const dir = path.join(ROOT, "entries", user.username);
    if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });

    const profilePath = path.join(dir, "PROFILE.md");
    const content = generateProfile(user);
    fs.writeFileSync(profilePath, content);
    console.log(`Generated PROFILE.md for ${user.username}`);
  }
}

generateProfiles();
